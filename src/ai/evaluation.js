export function evaluate(board, turn) {
  let score = 0;
  for (let row = 0; row < 8; row += 1) {
    for (let col = 0; col < 8; col += 1) {
      const symbol = board[row][col];
      if (!symbol) continue;
      const piece = symbol.toLowerCase();
      const color = symbol === symbol.toUpperCase() ? 'w' : 'b';
      const value = PieceValue[piece] || 0;
      const table = POSITIONAL[piece];
      const index = color === 'w' ? row : 7 - row;
      const positional = table ? table[index][col] : 0;
      score += color === 'w' ? value + positional : -(value + positional);
    }
  }
  return turn === 'w' ? score : -score;
}
